#### vytvářím n-gramy ---------------------------------------------------------

for(n in 2:5){
    
    n_grams <- unlist(lapply(
        my_splitted_sentences,
        function(x) getNGrams(x, n)
    ))
    
    assign(
        paste(
            setNames(
                c("bi", "tri", "quadri", "penta"),
                c(2, 3, 4, 5)
            )[as.character(n)],
            "grams",
            sep = "_"
        ),
        n_grams
    )
    
    flush.console()
    print(
        paste(
            n,
            "-gramy jsou zkompletovány.",
            sep = ""
        )
    )
    
}


